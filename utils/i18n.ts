export function getDeviceLanguage(): SupportedLanguage {
  // expo-localization is optional at runtime (dev clients can miss the native module).
  try {
    // eslint-disable-next-line @typescript-eslint/no-var-requires
    const Localization = require('expo-localization') as typeof import('expo-localization');
    const locale = Localization.getLocales?.()?.[0]?.languageCode?.toLowerCase?.() || 'fr';
    if (locale.startsWith('ar')) return 'ar';
    if (locale.startsWith('en')) return 'en';
    return 'fr';
  } catch {
    // Fallback without expo-localization
    const locale =
      (Intl as any)?.DateTimeFormat?.()?.resolvedOptions?.()?.locale?.toLowerCase?.() ||
      'fr';
    if (locale.startsWith('ar')) return 'ar';
    if (locale.startsWith('en')) return 'en';
    return 'fr';
  }
}

